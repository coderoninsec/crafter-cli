# -------------------------
# SPINNER FUNCTION
# -------------------------
spinner() {
    local pid=$!
    local spin='-\|/'
    local i=0

    while kill -0 $pid 2>/dev/null; do
        i=$(( (i+1) %4 ))
        printf "\r${CYAN}Installing... ${spin:$i:1}${RESET}"
        sleep .1
    done

    printf "\r"
}

